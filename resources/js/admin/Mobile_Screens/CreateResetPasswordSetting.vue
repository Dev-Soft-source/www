import Editor from "@tinymce/tinymce-vue";
import axios from "axios";
import ErrorHandling from "../../ErrorHandling.js";
export default {
    data() {
        return {
            activeLanguageId: null,
            languages: [],
            form: {},
            validationErros: new ErrorHandling(),
            collapseStates: [true, false, false, false, false, false, false],
            loading: false,
            editorConfig: {
                height: 250,
                menubar: false,
                plugins: [
                    "advlist autolink lists link image charmap print preview anchor image code table",
                    "searchreplace visualblocks code fullscreen",
                    "insertdatetime media table paste code help wordcount",
                ],
                toolbar:
                    "undo redo | formatselect | bold italic backcolor | \
                alignleft aligncenter alignright alignjustify | \
                bullist numlist outdent indent | removeformat | table | image | code | link | help",
            },
        };
    },
    components: {
        editor: Editor,
    },
    created() {
        this.fetchLanguages();
    },
    methods: {
        getCurrentValue(name) {
            return this.form[name] &&
                this.form[name][`${name}_${this.activeLanguageId}`]
                ? this.form[name][`${name}_${this.activeLanguageId}`]
                : "";
        },
        handleSelectionChange(language, key) {
            this.handleInput(
                tinymce.get(`${key}_${language.id}`).getContent(),
                language,
                key
            );
        },
        handleInput(value, language, key) {
            if (this.form.hasOwnProperty(key)) {
                this.form[key][`${key}_${language.id}`] = value;
            } else {
                this.form[key] = {};
                this.form[key][`${key}_${language.id}`] = value;
            }
        },
        updateLanguageId(language) {
            this.activeLanguageId = language.id;
        },
        fetchLanguages() {
            axios
                .get(`${process.env.MIX_ADMIN_API_URL}languages?getAll=1`)
                .then((res) => {
                    if (res?.data?.status == "Success") {
                        this.languages = res?.data?.data;
                        let defaultLang = this.languages.filter(
                            (x) => x.is_default == "1"
                        );
                        this.activeLanguageId = defaultLang?.[0]?.id || null;
                        let languages = res?.data?.data;
                        languages.map((language) => {
                            this.handleInput("", language, "main_heading");
                            this.handleInput("", language, "main_label");
                            this.handleInput("", language, "password_label");
                            this.handleInput("", language, "password_placeholder");
                            this.handleInput("", language, "confirm_password_label");
                            this.handleInput("", language, "confirm_password_placeholder");
                            this.handleInput("", language, "button_label");
                        });
                        this.fetchResetPasswordPageSetting();
                    }
                });
        },
        fetchResetPasswordPageSetting() {
            axios
                .get(`${process.env.MIX_ADMIN_API_URL}get-mobile-reset-password-page-setting`)
                .then((res) => {
                    if (res?.data?.status == "Success") {
                        let mobile_reset_password_setting_detail =
                            res?.data?.data?.mobile_reset_password_setting_detail || [];
                        mobile_reset_password_setting_detail.map((setting) => {
                            this.handleInput(
                                setting?.main_heading,
                                setting?.language,
                                "main_heading"
                            );
                            this.handleInput(
                                setting?.main_label,
                                setting?.language,
                                "main_label"
                            );
                            this.handleInput(
                                setting?.password_label,
                                setting?.language,
                                "password_label"
                            );
                            this.handleInput(
                                setting?.password_placeholder,
                                setting?.language,
                                "password_placeholder"
                            );
                            this.handleInput(
                                setting?.confirm_password_label,
                                setting?.language,
                                "confirm_password_label"
                            );
                            this.handleInput(
                                setting?.confirm_password_placeholder,
                                setting?.language,
                                "confirm_password_placeholder"
                            );
                            this.handleInput(
                                setting?.button_label,
                                setting?.language,
                                "button_label"
                            );
                        });
                    }
                });
        },
        updatePageSetting() {
            this.loading = true;
            axios
                .post(
                    `${process.env.MIX_ADMIN_API_URL}update-mobile-reset-password-page-setting`,
                    this.form
                )
                .then((res) => {
                    if (res?.data?.status == "Success") {
                        this.validationErros = new ErrorHandling();
                        helper.swalSuccessMessage(res.data.message);
                    } else {
                        helper.swalErrorMessage(res.data.message);
                    }
                    this.loading = false;
                })
                .catch((error) => {
                    this.validationErros = new ErrorHandling();
                    if (error.response && error.response.status == 422) {
                        this.validationErros.record(error.response.data.errors);
                    } else if (
                        error.response &&
                        error.response.data &&
                        error.response.data.status == "Error"
                    ) {
                        helper.swalErrorMessage(error.response.data.message);
                    }
                    this.loading = false;
                })
                .finally(() => (this.loading = false));
        },
        checkValidationError(validationErros, language) {
            return (
                validationErros.has(
                    `main_heading.main_heading_${language.id}`
                ) ||
                validationErros.has(
                    `main_label.main_label_${language.id}`
                ) ||
                validationErros.has(
                    `password_label.password_label_${language.id}`
                ) ||
                validationErros.has(
                    `password_placeholder.password_placeholder_${language.id}`
                ) ||
                validationErros.has(
                    `confirm_password_label.confirm_password_label_${language.id}`
                ) ||
                validationErros.has(
                    `confirm_password_placeholder.confirm_password_placeholder_${language.id}`
                ) ||
                validationErros.has(
                    `button_label.button_label_${language.id}`
                )
            );
        },
    },
